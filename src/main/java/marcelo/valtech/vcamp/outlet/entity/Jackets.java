public class Jackets {
	private final String jSize;
	private final String color;
	
	
	public Jackets(String jSize, String color) {
		super();
		this.jSize = jSize;
		this.color = color;
	}
	public String getjSize() {
		return jSize;
	}
	public String getColor() {
		return color;
	}
	
	
	
}
